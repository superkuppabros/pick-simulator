export function convertIdToSv64(id: number): string {
  const remainders: number[] = [];
  let quotient = id;
  while (quotient > 63) {
    remainders.push(quotient % 64);
    quotient = Math.floor(quotient / 64);
  }
  remainders.push(quotient);
  remainders.reverse();
  const sv64String = remainders.map(remainder => sv64table[remainder]).join("");
  return sv64String;
}
